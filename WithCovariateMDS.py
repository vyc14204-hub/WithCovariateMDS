    def plot_overlay_with_arrows(self):
        """
        [Plot Method 4] Overlays configurations with arrows indicating movement.
        Labels are placed automatically beyond the arrow direction.
        """
        if self.cov_X is None or self.std_X is None:
            raise ValueError("Both models must be estimated.")

        plt.figure(figsize=(9, 9))
        plt.axhline(0, color='lightgray', lw=0.5, ls='--')
        plt.axvline(0, color='lightgray', lw=0.5, ls='--')

        plt.scatter(self.std_X[:, 0], self.std_X[:, 1], facecolors='none', edgecolors='gray',
                    s=100, zorder=2, linestyles='dashed', label='Standard MDS')

        plt.scatter(self.cov_X[:, 0], self.cov_X[:, 1], c='royalblue',
                    s=100, zorder=3, edgecolors='white', label='Covariate MDS')

        max_coord = max(np.max(np.abs(self.std_X)), np.max(np.abs(self.cov_X)))
        offset_dist = max_coord * 0.08

        for i in range(len(self.items)):
            # Draw arrows
            plt.annotate('',
                         xy=(self.cov_X[i, 0], self.cov_X[i, 1]),
                         xytext=(self.std_X[i, 0], self.std_X[i, 1]),
                         arrowprops=dict(arrowstyle='->', color='gray', lw=1.5, alpha=0.7,
                                         shrinkA=8, shrinkB=8),
                         zorder=1)

            # Text positioning on the extension of the vector
            dx = self.cov_X[i, 0] - self.std_X[i, 0]
            dy = self.cov_X[i, 1] - self.std_X[i, 1]
            length = np.hypot(dx, dy)

            if length > 1e-5:
                text_x = self.cov_X[i, 0] + (dx / length) * offset_dist
                text_y = self.cov_X[i, 1] + (dy / length) * offset_dist
            else:
                orig_len = np.hypot(self.cov_X[i, 0], self.cov_X[i, 1])
                text_x = self.cov_X[i, 0] + (self.cov_X[i, 0] / (orig_len + 1e-10)) * offset_dist
                text_y = self.cov_X[i, 1] + (self.cov_X[i, 1] / (orig_len + 1e-10)) * offset_dist

            plt.text(text_x, text_y, self.items[i],
                     ha='center', va='center', size=10, zorder=4)

        ax = plt.gca()
        ax.spines['top'].set_visible(False)
        ax.spines['right'].set_visible(False)

        plt.title("Configuration Shift (with Arrows)", fontsize=15, y=1.05)
        plt.legend(loc='upper right', frameon=False)
        ax.set_aspect('equal', adjustable='box')

        plt.xlim(-max_coord*1.35, max_coord*1.35)
        plt.ylim(-max_coord*1.35, max_coord*1.35)

        plt.show()
        print("\n" * 3)
        plt.close()

